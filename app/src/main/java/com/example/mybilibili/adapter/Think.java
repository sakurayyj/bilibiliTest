package com.example.mybilibili.adapter;


public class Think {
    private int civ;
    private String txt;

    public Think(int civ,String txt){
        this.civ = civ;
        this.txt = txt;
    }

    public int getCiv() {
        return civ;
    }

    public void setCiv(int civ) {
        this.civ = civ;
    }

    public String getTxt() {
        return txt;
    }

    public void setTxt(String txt) {
        this.txt = txt;
    }
}
